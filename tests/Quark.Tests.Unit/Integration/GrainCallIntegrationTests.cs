// ---------------------------------------------------------------------------
// 1. Define the grain interface (Orleans-compatible: IGrainWithStringKey)
// ---------------------------------------------------------------------------

public interface ICounterGrain : IGrainWithStringKey
{
    Task<long> IncrementAsync();
    Task<long> GetValueAsync();
    Task ResetAsync();
}

// ---------------------------------------------------------------------------
// 2. Implement the grain
// ---------------------------------------------------------------------------

public sealed class CounterGrain : Grain, ICounterGrain
{
    private long _value;

    public Task<long> IncrementAsync()
    {
        _value++;
        return Task.FromResult(_value);
    }

    public Task<long> GetValueAsync() => Task.FromResult(_value);

    public Task ResetAsync()
    {
        _value = 0;
        return Task.CompletedTask;
    }
}

// ---------------------------------------------------------------------------
// 3. Method invoker (normally generated by Quark.CodeGenerator)
// ---------------------------------------------------------------------------

public sealed class CounterGrainMethodInvoker : IGrainMethodInvoker
{
    public const uint IncrementMethodId = 0;
    public const uint GetValueMethodId = 1;
    public const uint ResetMethodId = 2;

    public async ValueTask<object?> Invoke(Grain grain, uint methodId, object?[]? arguments)
    {
        var counter = (CounterGrain)grain;
        return methodId switch
        {
            IncrementMethodId => await counter.IncrementAsync(),
            GetValueMethodId => await counter.GetValueAsync(),
            ResetMethodId => await counter.ResetAsync().ContinueWith(_ => (object?)null),
            _ => throw new NotSupportedException($"Unknown method id {methodId}")
        };
    }
}

// ---------------------------------------------------------------------------
// 4. Proxy (normally generated by Quark.CodeGenerator)
// ---------------------------------------------------------------------------

public sealed class CounterGrainProxy : ICounterGrain
{
    private readonly GrainId _grainId;
    private readonly IGrainCallInvoker _invoker;

    public CounterGrainProxy(GrainId grainId, IGrainCallInvoker invoker)
    {
        _grainId = grainId;
        _invoker = invoker;
    }

    public Task<long> IncrementAsync() =>
        _invoker.InvokeAsync<long>(_grainId, CounterGrainMethodInvoker.IncrementMethodId);

    public Task<long> GetValueAsync() =>
        _invoker.InvokeAsync<long>(_grainId, CounterGrainMethodInvoker.GetValueMethodId);

    public Task ResetAsync() =>
        _invoker.InvokeVoidAsync(_grainId, CounterGrainMethodInvoker.ResetMethodId);
}

// ---------------------------------------------------------------------------
// 5. Test fixture that directly wires DI without IHost
// ---------------------------------------------------------------------------

public sealed class GrainCallFixture : IAsyncDisposable
{
    private readonly ServiceProvider _serviceProvider;
    private readonly GrainActivationTable _activationTable;

    public IClusterClient Client { get; }

    public GrainCallFixture()
    {
        var services = new ServiceCollection();

        // Logging (NullLogger so tests don't need log infrastructure)
        services.AddLogging();

        // SiloRuntimeOptions
        services.Configure<SiloRuntimeOptions>(o =>
        {
            o.ClusterId = "test";
            o.ServiceId = "integration";
            o.SiloName = "silo0";
        });

        // Runtime core services (minus the hosted service)
        services.AddSingleton<LifecycleSubject>();
        services.AddSingleton<GrainTypeRegistry>();
        services.AddSingleton<IGrainTypeRegistry>(sp => sp.GetRequiredService<GrainTypeRegistry>());
        services.AddSingleton<InMemoryGrainDirectory>();
        services.AddSingleton<IGrainDirectory>(sp => sp.GetRequiredService<InMemoryGrainDirectory>());
        services.AddSingleton<IGrainActivator, DefaultGrainActivator>();
        services.AddSingleton<GrainActivationTable>();
        services.AddSingleton<GrainMethodInvokerRegistry>();
        services.AddSingleton<IGrainMethodInvokerRegistry>(sp =>
            sp.GetRequiredService<GrainMethodInvokerRegistry>());

        // Register grain
        services.AddTransient<CounterGrain>();

        // Register method invoker
        services.AddSingleton<CounterGrainMethodInvoker>();

        // Client-side registries
        services.AddSingleton<GrainProxyFactoryRegistry>();
        services.AddSingleton<GrainInterfaceTypeRegistry>();

        _serviceProvider = services.BuildServiceProvider();

        // Apply deferred registrations (normally done by hosted services)
        var typeRegistry = _serviceProvider.GetRequiredService<GrainTypeRegistry>();
        typeRegistry.Register(new GrainType("CounterGrain"), typeof(CounterGrain));

        var invokerRegistry = _serviceProvider.GetRequiredService<GrainMethodInvokerRegistry>();
        invokerRegistry.Register(typeof(CounterGrain),
            _serviceProvider.GetRequiredService<CounterGrainMethodInvoker>());

        var proxyRegistry = _serviceProvider.GetRequiredService<GrainProxyFactoryRegistry>();
        var interfaceRegistry = _serviceProvider.GetRequiredService<GrainInterfaceTypeRegistry>();
        interfaceRegistry.Register(typeof(ICounterGrain), new GrainType("CounterGrain"));
        proxyRegistry.Register<ICounterGrain, CounterGrainProxy>(
            (grainId, invoker) => new CounterGrainProxy(grainId, invoker));

        // Construct LocalGrainCallInvoker manually (avoid hosted service dependency)
        _activationTable = _serviceProvider.GetRequiredService<GrainActivationTable>();
        var activator = _serviceProvider.GetRequiredService<IGrainActivator>();
        var directory = _serviceProvider.GetRequiredService<IGrainDirectory>();
        var methodInvokerReg = _serviceProvider.GetRequiredService<IGrainMethodInvokerRegistry>();
        var siloOptions = _serviceProvider.GetRequiredService<IOptions<SiloRuntimeOptions>>();
        var logger = NullLogger<LocalGrainCallInvoker>.Instance;

        // Build a LocalGrainFactory so it's available for grain inter-calls
        var dummyInvoker = new DeferredLocalGrainCallInvoker();
        var localFactory = new LocalGrainFactory(proxyRegistry, interfaceRegistry, dummyInvoker);

        var callInvoker = new LocalGrainCallInvoker(
            _activationTable, activator, typeRegistry, directory,
            methodInvokerReg, localFactory, _serviceProvider, siloOptions, logger);

        // Wire back the real invoker
        dummyInvoker.SetInvoker(callInvoker);

        // Build the client
        var factory = new LocalGrainFactory(proxyRegistry, interfaceRegistry, callInvoker);
        Client = new LocalClusterClient(factory);
    }

    public async ValueTask DisposeAsync()
    {
        await _activationTable.DisposeAsync();
        await _serviceProvider.DisposeAsync();
    }

    // Wrapper that resolves the invoker lazily to break the circular construction
    private sealed class DeferredLocalGrainCallInvoker : IGrainCallInvoker
    {
        private IGrainCallInvoker? _inner;
        public void SetInvoker(IGrainCallInvoker invoker) => _inner = invoker;
        public Task<object?> InvokeAsync(GrainId id, uint method, object?[]? args = null, CancellationToken ct = default)
            => _inner!.InvokeAsync(id, method, args, ct);
        public Task<TResult> InvokeAsync<TResult>(GrainId id, uint method, object?[]? args = null, CancellationToken ct = default)
            => _inner!.InvokeAsync<TResult>(id, method, args, ct);
        public Task InvokeVoidAsync(GrainId id, uint method, object?[]? args = null, CancellationToken ct = default)
            => _inner!.InvokeVoidAsync(id, method, args, ct);
    }
}

// ---------------------------------------------------------------------------
// 6. End-to-end integration tests
// ---------------------------------------------------------------------------

public sealed class GrainCallIntegrationTests : IAsyncLifetime
{
    private GrainCallFixture _fixture = null!;

    public Task InitializeAsync()
    {
        _fixture = new GrainCallFixture();
        return Task.CompletedTask;
    }

    public Task DisposeAsync() => _fixture.DisposeAsync().AsTask();

    private ICounterGrain GetCounter(string key) => _fixture.Client.GetGrain<ICounterGrain>(key);

    [Fact]
    public async Task GetGrain_ReturnsProxy_WithCorrectInterface()
    {
        var grain = GetCounter("test-grain");
        Assert.NotNull(grain);
        Assert.IsAssignableFrom<ICounterGrain>(grain);
    }

    [Fact]
    public async Task IncrementAsync_IncrementsCounter()
    {
        var grain = GetCounter("counter-1");
        var result = await grain.IncrementAsync();
        Assert.Equal(1L, result);
    }

    [Fact]
    public async Task MultipleIncrements_SequentialExecution()
    {
        var grain = GetCounter("counter-seq");
        var v1 = await grain.IncrementAsync();
        var v2 = await grain.IncrementAsync();
        var v3 = await grain.IncrementAsync();
        Assert.Equal(1L, v1);
        Assert.Equal(2L, v2);
        Assert.Equal(3L, v3);
    }

    [Fact]
    public async Task GetValue_SameKey_ReturnsSameActivation()
    {
        var grain1 = GetCounter("shared-grain");
        var grain2 = GetCounter("shared-grain");
        await grain1.IncrementAsync();
        await grain1.IncrementAsync();
        Assert.Equal(2L, await grain2.GetValueAsync());
    }

    [Fact]
    public async Task ResetAsync_ResetsCounterToZero()
    {
        var grain = GetCounter("reset-grain");
        await grain.IncrementAsync();
        await grain.IncrementAsync();
        await grain.ResetAsync();
        Assert.Equal(0L, await grain.GetValueAsync());
    }

    [Fact]
    public async Task DifferentKeys_IndependentActivations()
    {
        var grainA = GetCounter("key-a");
        var grainB = GetCounter("key-b");
        await grainA.IncrementAsync();
        await grainA.IncrementAsync();
        await grainB.IncrementAsync();
        Assert.Equal(2L, await grainA.GetValueAsync());
        Assert.Equal(1L, await grainB.GetValueAsync());
    }

    [Fact]
    public async Task ConcurrentCalls_SameGrain_AreSerialized()
    {
        var grain = GetCounter("concurrent-grain");
        var tasks = Enumerable.Range(0, 20)
            .Select(_ => grain.IncrementAsync())
            .ToList();
        var results = await Task.WhenAll(tasks);
        Assert.Equal(20L, await grain.GetValueAsync());
        Assert.Equal(
            Enumerable.Range(1, 20).Select(i => (long)i).OrderBy(x => x),
            results.OrderBy(x => x));
    }

    [Fact]
    public async Task GetValue_BeforeAnyCall_ReturnsZero()
    {
        var grain = GetCounter("fresh-grain");
        Assert.Equal(0L, await grain.GetValueAsync());
    }
}
